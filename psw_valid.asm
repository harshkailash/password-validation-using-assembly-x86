ASSUME CS:CODE,DS:DATA
DATA SEGMENT
MSG1 DB 'ENTER your PASSWORD : $'
MSG2 DB 'YOU HAVE ENTERED WRONG PASSWORD !!! $'
MSG3 DB 'YOU HAVE ENTERED CORRECT PASSWORD ! $'
MSG4 DB 'PASSWORD LENGTH IS NOT EQUAL $'
STR1 DB "123456"
P1 LABEL BYTE
M1 DB 0FFH
L1 DB ?
P11 DB 0FFH DUP ('$')
DATA ENDS
DISPLAY MACRO MSG
MOV AH,9
LEA DX,MSG
INT 21H
ENDM
CODE SEGMENT
ASSUME CS:CODE,DS:DATA
START:
MOV AX,DATA
MOV DS,AX
DISPLAY MSG1
LEA DX,P1
MOV AH,0AH
INT 21H
CMP L1,6
JNE NOTEQUAL
LEA SI,STR1
LEA DI,P11
MOV CX,5
CHECK:
MOV AL,[SI]
CMP [DI],AL
JNE NOPASWD
INC SI
INC DI
LOOP CHECK
DISPLAY MSG3
JMP EXIT
NOTEQUAL:
DISPLAY MSG4
NOPASWD:
DISPLAY MSG2
JMP EXIT
EXIT: MOV AH,4CH
INT 21H
HLT
CODE ENDS
END START
